    @Override
    public void field2Object(Object obj) {
        ColaboradorFilter colabFilter = (ColaboradorFilter) obj;
        colabFilter.setTitle(null);
        Colaborador colab = colabFilter.getColaborador();
        colab.setCidade(cidadeField.getText());
        colab.setRazao(razaoField.getText());
        colab.setUf(ufField.getText());

        FuncaoColaborador funcao = (FuncaoColaborador) vendedorComboBox.getModel().getSelectedItem();
        if (funcao != null) {
            if (funcao.getIdfuncao() != null) {
                colabFilter.setTitle("Fun\u00E7\u00E3o :" + funcao.getNome());
            }
        }

    }

