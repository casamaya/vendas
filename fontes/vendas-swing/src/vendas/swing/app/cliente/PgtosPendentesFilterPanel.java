    @Override
    public void field2Object(Object obj) {
        ClientesFilter filter = (ClientesFilter)obj;
        filter.setVendedor((Vendedor) vendedorComboBox.getModel().getSelectedItem());
        if (vendedorRadioButton.isSelected())
            filter.setOrder(0);
        if (clienteRadioButton.isSelected())
            filter.setOrder(1);
        if (valorRadioButton.isSelected())
            filter.setOrder(2);
        filter.setDecrescente(descCheckBox.isSelected());
    }

